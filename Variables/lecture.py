
### Problema 1: Suma de dos números
#Escribe un programa que solicite al usuario dos números enteros y luego muestre la suma de los dos números.


# Solicitar al usuario dos números enteros
num1 = int(input("Ingrese el primer número: "))
num2 = int(input("Ingrese el segundo número: "))

# Calcular la suma de los dos números
suma = num1 + num2

# Mostrar el resultado
print("La suma de", num1, "y", num2, "es:", suma)


### Problema 2: Área de un rectángulo
#Escribe un programa que solicite al usuario la longitud y el ancho de un rectángulo y luego calcule y muestre el área del rectángulo.


# Solicitar al usuario la longitud y el ancho del rectángulo
longitud = float(input("Ingrese la longitud del rectángulo: "))
ancho = float(input("Ingrese el ancho del rectángulo: "))

# Calcular el área del rectángulo
area = longitud * ancho

# Mostrar el resultado
print("El área del rectángulo es:", area)


### Problema 3: Conversión de temperaturas
#Escribe un programa que solicite al usuario una temperatura en grados Celsius y luego la convierta a grados Fahrenheit utilizando la fórmula: Fahrenheit = (Celsius * 9/5) + 32. Muestra el resultado.


# Solicitar al usuario la temperatura en grados Celsius
celsius = float(input("Ingrese la temperatura en grados Celsius: "))

# Convertir la temperatura a grados Fahrenheit
fahrenheit = (celsius * 9/5) + 32

# Mostrar el resultado
print("La temperatura en grados Fahrenheit es:", fahrenheit)


